end
% ============== ber'(x) for abs(x) > 8.0 ==============
temp1 = exp(u)*(fac2*(SRsn + SIsn) - fac1*(SRsn - SIsn));
temp2 = exp(-u)*(fac3*(SRqn + SIqn) + fac4*(SRqn - SIqn));
beid  = fac*(temp1 - temp2);
if x < 0
    beid = - beid;
end
% ============== CHEBY ===============
function value = cheby(coeff,xf,select)
n = length(coeff);
x = 2*xf;
b1 = 0; b0 = coeff(n);
for k = n-1:-1:1
    b2 = b1;
    b1 = b0;
    b0 = coeff(k) - b2 + x*b1;
end
if select < 0.5
    value = b0 - b1;
else
    value = (b0 - b2)*0.5;
end

