select = 1;
SIsn = cheby(Isn,xf,select);
SRsn = cheby(Rsn,xf,select);
SIqn = cheby(Iqn,xf,select);
SRqn = cheby(Rqn,xf,select);
u = xa/sqrt(2); fac = 0.5/sqrt(pi*xa);
fac1 = cos(3*pi/8 + u); fac2 = sin(3*pi/8 + u);
fac3 = cos(pi/8 + u); fac4 = sin(pi/8 + u);
% ============== ber'(x) for abs(x) > 8.0 ==============
temp1 = exp(u)*(fac1*(SRsn + SIsn) + fac2*(SRsn - SIsn));
temp2 = exp(-u)*(fac3*(SRqn - SIqn) - fac4*(SRqn + SIqn));
berd  = fac*(temp1 + temp2);
if x < 0
